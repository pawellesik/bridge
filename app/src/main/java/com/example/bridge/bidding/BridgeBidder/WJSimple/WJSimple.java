package com.example.bridge.bidding.BridgeBidder.WJSimple;

import com.example.bridge.bidding.BridgeBidder.Tools.Bidder;
import com.example.bridge.bidding.BridgeBidder.Tools.IBiddingSystem;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionCalls;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionRole;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionState;
import com.example.bridge.bidding.BridgeBidder.Tools.Range;

public class WJSimple extends Bidder implements IBiddingSystem {
    @Override
    public PositionCalls getPositionCalls(PositionState ps) {
        if (ps.getRole() == PositionRole.Opener && ps.getRoleRound() == 1) {
            return OpenWJSimple.getOpenPositionCalls(ps);
        } else {
            PositionCalls calls = new PositionCalls(ps);
            calls.addRules(CompeteWJSimple::compBids);
            return calls;
        }
    }

    public static final Range PAIR_GAME_INVITE = new Range(23, 24);
    public static final Range PAIR_GAME = new Range(25, 31);
    public static final Range PAIR_MINOR_GAME = new Range(27, 31);
    public static final Range PAIR_LOW_GAME = new Range(19, 24);
    public static final Range LESS_THAN_OVERCALL = new Range(0, 17);
    public static final Range OVERCALL_1_LEVEL = new Range(7, 17);
    public static final Range OVERCALL_STRONG_2_LEVEL = new Range(13, 17);
    public static final Range OVERCALL_WEAK_2_LEVEL = new Range(7, 11);
    public static final Range OVERCALL_WEAK_3_LEVEL = new Range(7, 11);
}





























































































































































































































































































































































































































































































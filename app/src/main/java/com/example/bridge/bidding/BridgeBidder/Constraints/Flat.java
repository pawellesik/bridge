package com.example.bridge.bidding.BridgeBidder.Constraints;

import com.example.bridge.bidding.BridgeBidder.Tools.Call;
import com.example.bridge.bidding.BridgeBidder.Tools.HandConstraint;
import com.example.bridge.bidding.BridgeBidder.Tools.HandSummary;
import com.example.bridge.bidding.BridgeBidder.Tools.IDescribeConstraint;
import com.example.bridge.bidding.BridgeBidder.Tools.IShowsHand;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionState;
import com.example.bridge.bidding.BridgeBidder.Tools.Suit;

/**
 * Klasa sprawdzająca czy ręka ma charakter "płaski" (zrównoważony).
 * Ręka płaska to taka, która nie posiada krótkości (singli ani renonsów),
 * a jej układ to zazwyczaj 4-3-3-3.
 */
public class Flat {
    /**
     * Weryfikuje czy ręka spełnia warunek płaskości bez pokazywania tego partnerowi.
     */
    public static class IsFlat extends HandConstraint {
        protected final boolean desiredValue;

        public IsFlat(boolean desiredValue) {
            this.desiredValue = desiredValue;
        }

        @Override
        public boolean conforms(Call call, PositionState ps, HandSummary hs) {
            return hs.getIsFlat() == null || hs.getIsFlat() == desiredValue;
        }
    }

    /**
     * Informuje system i partnera o płaskim charakterze ręki.
     * Dodatkowo wymusza zakres 3-4 karty w każdym kolorze dla ręki płaskiej.
     */
    public static class ShowsFlat extends IsFlat implements IShowsHand, IDescribeConstraint {
        public ShowsFlat(boolean desiredValue) {
            super(desiredValue);
        }

        @Override
        public void showHand(Call call, PositionState ps, HandSummary.ShowState showHand) {
            showHand.showIsFlat(desiredValue);
            if (desiredValue) {
                for (Suit suit : Suit.values()) {
                    showHand.getSuits().get(suit).showShape(3, 4);
                }
            }
        }

        @Override
        public String describe(Call call, PositionState ps) {
            return desiredValue ? "flat" : "not flat";
        }
    }
}





























































































































































































































































































































































































































































































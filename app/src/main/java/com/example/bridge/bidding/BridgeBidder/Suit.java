package com.example.bridge.bidding.BridgeBidder;

public enum Suit {
    Clubs, Diamonds, Hearts, Spades;

    public String toLetter() {
        switch (this) {
            case Clubs: return "C";
            case Diamonds: return "D";
            case Hearts: return "H";
            case Spades: return "S";
            default: return "";
        }
    }

    public String toSymbol() {
        switch (this) {
            case Clubs: return "♣";
            case Diamonds: return "♦";
            case Hearts: return "♥";
            case Spades: return "♠";
            default: return "";
        }
    }

    public Strain toStrain() {
        switch (this) {
            case Clubs: return Strain.Clubs;
            case Diamonds: return Strain.Diamonds;
            case Hearts: return Strain.Hearts;
            case Spades: return Strain.Spades;
            default: throw new RuntimeException("Invalid suit");
        }
    }

    public boolean isMajor() {
        return this == Hearts || this == Spades;
    }

    public boolean isMinor() {
        return this == Clubs || this == Diamonds;
    }
}





























































































































































































































































































































































































































































































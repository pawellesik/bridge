package com.example.bridge.bidding.BridgeBidder.Constraints;

import com.example.bridge.bidding.BridgeBidder.Tools.Call;
import com.example.bridge.bidding.BridgeBidder.Tools.HandConstraint;
import com.example.bridge.bidding.BridgeBidder.Tools.HandSummary;
import com.example.bridge.bidding.BridgeBidder.Tools.IDescribeConstraint;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionState;
import com.example.bridge.bidding.BridgeBidder.Tools.Range;
import com.example.bridge.bidding.BridgeBidder.Tools.Suit;

/**
 * Wykrywa krótkość (renons lub singiel) w JAKIMKOLWIEK kolorze.
 * Bardzo przydatne przy licytowaniu Splinterów lub sprawdzaniu wartości układowych.
 */
public class HasShortness extends HandConstraint implements IDescribeConstraint {
    private final int min; // Zazwyczaj 0
    private final int max; // Zazwyczaj 1 (dla singla/renonsu)

    /**
     * @param min Minimalna długość (np. 0).
     * @param max Maksymalna długość (np. 1).
     */
    public HasShortness(int min, int max) {
        this.min = min;
        this.max = max;
    }

    /**
     * Sprawdza wszystkie kolory w poszukiwaniu takiego, który mieści się w zakresie [min, max].
     */
    @Override
    public boolean conforms(Call call, PositionState ps, HandSummary hs) {
        for (Suit s : Suit.values()) {
            Range shape = hs.getSuits().get(s).getShape();
            // Sprawdzamy czy MOŻLIWE jest, że ten kolor ma długość w zakresie [min, max]
            // W licytacji sprawdzamy czy istnieje chociaż jedna kombinacja, która spełnia warunek.
            if (shape.getMin() <= max && shape.getMax() >= min) {
                return true;
            }
        }
        return false;
    }

    @Override
    public String describe(Call call, PositionState ps) {
        if (min == 0 && max == 0) return "any void";
        if (min == 1 && max == 1) return "any singleton";
        if (min == 0 && max == 1) return "any shortness (0-1)";
        return "any suit with " + min + "-" + max + " cards";
    }
}





























































































































































































































































































































































































































































































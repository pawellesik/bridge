package com.example.bridge.bidding.BridgeBidder;

import java.util.function.BiFunction;

public class SimpleStaticConstraint extends StaticConstraint implements IDescribeConstraint {
    private final BiFunction<Call, PositionState, Boolean> eval;
    private final BiFunction<Call, PositionState, String> getDescription;
    private final String logDescription;

    public SimpleStaticConstraint(BiFunction<Call, PositionState, Boolean> eval) {
        this(eval, (call, ps) -> null, null);
    }

    public SimpleStaticConstraint(BiFunction<Call, PositionState, Boolean> eval, String description) {
        this(eval, (call, ps) -> description, null);
    }

    public SimpleStaticConstraint(BiFunction<Call, PositionState, Boolean> eval,
                                  BiFunction<Call, PositionState, String> getDescription) {
        this(eval, getDescription, null);
    }

    public SimpleStaticConstraint(BiFunction<Call, PositionState, Boolean> eval,
                                  BiFunction<Call, PositionState, String> getDescription,
                                  String logDescription) {
        this.eval = eval != null ? eval : (call, ps) -> true;
        this.getDescription = getDescription != null ? getDescription : (call, ps) -> null;
        this.logDescription = logDescription;
    }

    @Override
    public boolean conforms(Call call, PositionState ps) {
        return eval.apply(call, ps);
    }

    @Override
    public String describe(Call call, PositionState ps) {
        return getDescription.apply(call, ps);
    }

    @Override
    public String getLogDescription(Call call, PositionState ps) {
        if (logDescription != null) return logDescription;
        return super.getLogDescription(call, ps);
    }
}





























































































































































































































































































































































































































































































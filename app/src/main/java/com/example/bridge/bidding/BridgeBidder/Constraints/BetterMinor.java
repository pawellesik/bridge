package com.example.bridge.bidding.BridgeBidder.Constraints;

import com.example.bridge.bidding.BridgeBidder.Tools.Call;
import com.example.bridge.bidding.BridgeBidder.Tools.HandConstraint;
import com.example.bridge.bidding.BridgeBidder.Tools.HandSummary;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionState;
import com.example.bridge.bidding.BridgeBidder.Tools.Range;
import com.example.bridge.bidding.BridgeBidder.Tools.Suit;

/**
 * Klasa decydująca o wyborze "lepszego" koloru młodszego (Trefl lub Karo).
 * Stosowana przy otwarciach 1 w kolor młodszy, gdy gracz ma oba te kolory.
 * Przy równej długości 3-3 wybiera się zazwyczaj trefle, przy 4-4 kara.
 */
public class BetterMinor extends HandConstraint {
    private final Suit suit; // Kolor, który sprawdzamy czy jest "lepszy"

    public BetterMinor(Suit suit) {
        this.suit = suit;
    }

    @Override
    public boolean conforms(Call call, PositionState ps, HandSummary hs) {
        Suit better = getSuit(this.suit, call);
        if (better != null && (better == Suit.Clubs || better == Suit.Diamonds)) {
            Range shapeClubs = hs.getSuits().get(Suit.Clubs).getShape();
            Range shapeDiamonds = hs.getSuits().get(Suit.Diamonds).getShape();
            if (shapeClubs.getMin() != shapeClubs.getMax() || shapeDiamonds.getMin() != shapeDiamonds.getMax()) return true;
            if (shapeClubs.getMin() < shapeDiamonds.getMin()) return (better == Suit.Diamonds);
            if (shapeClubs.getMin() > shapeDiamonds.getMin()) return (better == Suit.Clubs);
            if (shapeClubs.getMin() < 4) return (better == Suit.Clubs);
            return (better == Suit.Diamonds);
        }
        return false;
    }
}





























































































































































































































































































































































































































































































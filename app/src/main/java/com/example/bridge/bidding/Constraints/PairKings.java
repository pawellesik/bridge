package com.example.bridge.bidding.Constraints;

import com.example.bridge.bidding.Tools.Call;
import com.example.bridge.bidding.Tools.HandConstraint;
import com.example.bridge.bidding.Tools.HandSummary;
import com.example.bridge.bidding.Tools.IDescribeConstraint;
import com.example.bridge.bidding.Tools.PositionState;

import java.util.*;

/**
 * Constraint sprawdzający łączną liczbę króli posiadanych przez parę.
 * Używany analogicznie do PairAces przy podejmowaniu decyzji o szlemie.
 */
public class PairKings extends HandConstraint implements IDescribeConstraint {
    private final int[] count; // Dopuszczalne sumy króli w parze

    public PairKings(int... count) {
        this.count = count;
    }

    @Override
    public boolean conforms(Call call, PositionState ps, HandSummary hs) {
        Set<Integer> ourKings = hs.getCountKings();
        Set<Integer> partnerKings = ps.getPartner().getPublicHandSummary().getCountKings();
        if (ourKings == null) {
            if (partnerKings == null) return true;
            return Arrays.stream(count).max().orElse(0) >= partnerKings.stream().min(Integer::compare).orElse(0);
        }
        if (partnerKings == null) {
            return Arrays.stream(count).max().orElse(0) >= ourKings.stream().min(Integer::compare).orElse(0);
        }
        for (int ourCount : ourKings) {
            for (int pCount : partnerKings) {
                for (int c : count) {
                    if (c == ourCount + pCount) return true;
                }
            }
        }
        return false;
    }

    @Override
    public String describe(Call call, PositionState ps) {
        String s = (count.length == 1 && count[0] == 1) ? "" : "s";
        String countStr = Arrays.toString(count).replace("[", "").replace("]", "").replace(",", " or");
        return countStr + " King" + s;
    }
}





























































































































































































































































































































































































































































































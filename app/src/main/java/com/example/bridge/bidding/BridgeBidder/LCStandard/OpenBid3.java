package com.example.bridge.bidding.BridgeBidder.LCStandard;

import com.example.bridge.bidding.BridgeBidder.Tools.Bid;
import com.example.bridge.bidding.BridgeBidder.Tools.Call;
import com.example.bridge.bidding.BridgeBidder.Tools.CallFeature;
import com.example.bridge.bidding.BridgeBidder.Tools.PositionState;

import java.util.ArrayList;
import java.util.List;

public class OpenBid3 extends Open {

    public static Iterable<CallFeature> thirdBid(PositionState ps) {
        List<CallFeature> bids = new ArrayList<>();
        bids.add(shows(Bid._1NT, BALANCED, points(Rebid1NT)));
        bids.add(shows(Bid._2NT, BALANCED, points(Rebid2NT)));

        // In constructive auction, if opener has already shown a minimum hand
        // and responder makes an invitational raise, opener should pass if minimum.
        bids.add(shows(Call.PASS, partner(isJump(1)), Minimum));

        for (CallFeature cf : Compete.compBids(ps)) {
            bids.add(cf);
        }
        return bids;
    }
}





























































































































































































































































































































































































































































































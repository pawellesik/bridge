package com.example.bridge.bidding.TestBridgeBidder;

import com.example.bridge.bidding.Tools.Strain;
import com.example.bridge.bidding.Tools.Suit;
import org.junit.Test;
import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertNull;

public class TestStrain {

    @Test
    public void toSuit() {
        assertEquals(Suit.Clubs, Strain.Clubs.toSuit());
        assertEquals(Suit.Diamonds, Strain.Diamonds.toSuit());
        assertEquals(Suit.Hearts, Strain.Hearts.toSuit());
        assertEquals(Suit.Spades, Strain.Spades.toSuit());
        assertNull(Strain.NoTrump.toSuit());
    }
}




























































































































































































































































































































































































































































































